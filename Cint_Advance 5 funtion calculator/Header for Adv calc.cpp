using namespace std;

//Functions using references

double addN(double rValOne, double rValTwo)
{

	return rValOne + rValTwo;
}
double subN(double rValOne, double rValTwo)
{
	return rValOne - rValTwo;
}

double multN(double rValOne, double rValTwo)
{
	return rValOne * rValTwo;
}

double divN(double rValOne, double rValTwo)
{
	return rValOne / rValTwo;
}

int addN(int rValOne, int rValTwo)
{
	return rValOne + rValTwo;
}
